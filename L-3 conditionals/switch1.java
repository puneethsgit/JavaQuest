public class switch1 {
    public static void main(String[] args) {
        int num = 11;
        switch(num){
            case 1:
                System.out.println("A");
                break;
            case 2:
                System.out.println("B");
                break;
            case 3:
                System.out.println("C");
                break;
            default:
                System.out.println("None");
        }
    }
}
